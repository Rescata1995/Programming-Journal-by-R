var numero = 1;
var numero2 = 5;

// Operadores básicos

// suma
var suma= numero + numero2;

// resta
var resta= numero - numero2;

// multiplicación
var mult = numero * numero2;

// división
var div = numero / numero2;

// Ahora, por medio de la función console.log(), vamos a concatenar una cadena de texto a cada una de nuestras variables, tal que así:

console.log("El resultado de nuestra suma es: " + suma);
console.log("El resultado de nuestra resta es: " + resta);
console.log("El resultado de nuestra multiplicación es: " + mult);
console.log("El resultado de nuestra división es: " + div);

// Ahora, veremos cómo se concatena con operadores de incremento (recuerde cuando incrementamos una unidad al contador en bucles en C)
// Operadores de incremento

suma ++; // Esto corresponde a adicionarle una unidad a nuestra variable "suma". Es decir, si "suma" es X, ahora es X + 1. 
console.log("Incrementamos en una unidad nuestra variable suma, ahora es: " + suma); // se llama: operador de incremento 

suma--; // Esto corresponde a restarle una unidad a nuestra variable "suma". Es decir, si "suma" ahora es X + 1, ahora es X nuevamente.
console.log("Disminuimos en una unidad nuestra variable 'suma', ahora vuelve a ser: " + suma); // se llama: operador de decremento








